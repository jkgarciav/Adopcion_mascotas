                        </h2>
                        <p>El hecho de que estés pensando en adoptar un animal de un refugio significa que eres una persona responsable y humanitaria. Pero antes de tomar la decisión de traer un amigo peludo a tu vida, tómate un momento para pensar en estos puntos:</p>
                        <p>¿Porqué quieres compartir tu vida con un animal? Es increí­ble cuántas personas no se preguntan esto tan simple antes de acoger uno. Adoptar un perro o gato simplemente porque es lo que la gente "hace" o porque los niños han estado lloriqueando por un cachorro generalmente termina en un gran error. No te olvides de que algunos de ellos pueden estar contigo 10, 15 o incluso 20 años.</p>
                        <p>¿Tienes tiempo? Perros, gatos y otros animales no pueden ser ignorados simplemente porque estés cansado u ocupado. Necesitan comida, agua, ejercicio, cariño y compañí­a cada dí­a de cada año. Muchos animales en los refugios están allí­ porque sus dueños no pensaron realmente cuánto tiempo llevaba cuidar de ellos.</p>
                        <p>¿Puedes permitirte económicamente una adopción? Los costes de mantener un animal pueden ser bastante elevados. Licencias, clases de entrenamiento, esterilización y castración, cuidados veterinarios, cepillados, juguetes, comida, arena de gatos, y otros gastos son cosas que suman rápidamente</p>
                        <p>¿Estás preparado para ocuparte de los problemas que puede ocasionar? Infestaciones de pulgas, muebles arañados, accidentes de animales que no están educados para estar en una casa y emergencias médicas inesperadas son lamentables pero son aspectos comunes a la tenencia de un animal.</p>
                        <p>¿Puedes tener uno en el lugar donde vives? Muchas comunidades de vecinos no permiten animales, y la mayorí­a de ellos tienen restricciones. Asegúrate de cuáles son antes de traer un compañero peludo a casa. También es posible que en tu casa, no todos estén conformes con la llegada de un animal. Es importante tomar la decisión en familia y evitar que pasado el tiempo te presionen para sacar al animal del hogar.</p>
                        <p>¿Son tus hábitos de vida adecuados para el animal que tienes en mente? El tamaño del mismo no es la única variable en la que tienes que pensar aquí­. Por ejemplo, algunos perros pequeños como los terriers son muy activos. Necesitan mucho ejercicio para poder calmarse, y generalmente ladran con cualquier ruido. Por otro lado, algunos perros grandes son muy tranquilos y están felices y contentos con sólo dormir en el sofá todo el dí­a. Antes de adoptar haz algo de investigación, de este modo te aseguras de elegir el animal que encajará con tu estilo de vida.</p>
                        <p>¿Sabes quién cuidará de él cuando estés de viaje o de vacaciones? Necesitarás tener amigos o vecinos de confianza o dinero para pagar a un refugio o un hostal.</p>
